// lib/core/documentation/documentation_guidelines.dart
/**
 * دليل توثيق الشيفرة البرمجية
 * ===========================
 * 
 * هذا الملف يحدد معايير وإرشادات توثيق الشيفرة البرمجية في المشروع.
 * يجب اتباع هذه الإرشادات في جميع ملفات المشروع لضمان توحيد أسلوب التوثيق.
 */

/**
 * قواعد عامة للتوثيق:
 * ------------------
 * 1. استخدام اللغة العربية للتعليقات والتوثيق
 * 2. توثيق جميع الفئات والدوال والمتغيرات العامة
 * 3. استخدام صيغة موحدة للتعليقات
 * 4. شرح الغرض من الكود وليس كيفية عمله (إلا إذا كان معقدًا)
 * 5. تحديث التوثيق عند تغيير الكود
 */

/**
 * صيغة توثيق الفئات:
 * -----------------
 * /// وصف الفئة وغرضها
 * /// 
 * /// تفاصيل إضافية عن الفئة (اختياري)
 * class ClassName {
 *   ...
 * }
 */

/**
 * صيغة توثيق الدوال:
 * ----------------
 * /// وصف الدالة وغرضها
 * /// 
 * /// المعاملات:
 * /// - [param1]: وصف المعامل الأول
 * /// - [param2]: وصف المعامل الثاني
 * /// 
 * /// القيمة المرجعة:
 * /// وصف القيمة المرجعة
 * /// 
 * /// الاستثناءات:
 * /// - ExceptionType: متى يتم رمي هذا الاستثناء
 * returnType functionName(param1, param2) {
 *   ...
 * }
 */

/**
 * صيغة توثيق المتغيرات:
 * -------------------
 * /// وصف المتغير وغرضه
 * final Type variableName = value;
 */

/**
 * صيغة توثيق المزودات (Providers):
 * ------------------------------
 * /// مزود لـ [ServiceName]
 * /// 
 * /// يوفر وصول إلى [ServiceName] في جميع أنحاء التطبيق
 * final serviceNameProvider = Provider<ServiceName>((ref) {
 *   ...
 * });
 */

/**
 * أمثلة على التوثيق الجيد:
 * ----------------------
 * 
 * مثال لتوثيق فئة:
 * 
 * /// خدمة التشفير المسؤولة عن تشفير وفك تشفير البيانات
 * /// 
 * /// توفر هذه الخدمة واجهة موحدة لجميع عمليات التشفير في التطبيق
 * class EncryptionService {
 *   ...
 * }
 * 
 * مثال لتوثيق دالة:
 * 
 * /// تشفير نص باستخدام خوارزمية AES-GCM
 * /// 
 * /// المعاملات:
 * /// - [plainText]: النص المراد تشفيره
 * /// - [password]: كلمة المرور المستخدمة لاشتقاق مفتاح التشفير
 * /// 
 * /// القيمة المرجعة:
 * /// [Result] يحتوي على النص المشفر في حالة النجاح أو رسالة خطأ في حالة الفشل
 * /// 
 * /// الاستثناءات:
 * /// - EncryptionException: في حالة فشل عملية التشفير
 * Future<Result<String>> encryptAesGcm(String plainText, String password) async {
 *   ...
 * }
 */

/**
 * ملاحظات إضافية:
 * --------------
 * - استخدم تعليقات TODO للإشارة إلى المهام المستقبلية
 * - استخدم تعليقات FIXME للإشارة إلى المشاكل التي تحتاج إلى إصلاح
 * - استخدم تعليقات NOTE للإشارة إلى ملاحظات مهمة
 */
